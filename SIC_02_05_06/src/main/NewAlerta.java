    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Agregar
        alerta=new AlertaInternacional(txtTitulo.getText(),jcbImportancia.getSelectedIndex(),txtDescripcion.getText(),jcbUbicacion.getSelectedItem().toString());
        alerta.insertarEnDB();
        mf.alertas.add(alerta);//try catch
        //mf.setTxtArea( mf.getTxtArea()+"\n"+alerta.toString());
        
        int opt=showOptionDialog(null, "Â¿Desea crear mas alertas?","Mas alertas",JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE,null,null,null);
            switch (opt) {
                case JOptionPane.YES_OPTION:
                    txtTitulo.requestFocus();
                    break;
                case JOptionPane.NO_OPTION:
                    this.dispose();
                    break;
                case JOptionPane.CANCEL_OPTION:
                    break;
                default:
                    break;
            }
    }//GEN-LAST:event_jButton2ActionPerformed

